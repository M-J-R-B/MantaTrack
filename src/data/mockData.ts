// Helper function to get price history for a specific price entry
export const getPriceHistoryForEntry = (priceEntryId: string): PriceHistory[] => {
  return priceHistory.filter(history => history.priceEntryId === priceEntryId);
};

// Helper function to check if a price is stale (older than 48 hours)
export const isPriceStale = (updatedAt: Date): boolean => {
  const now = new Date();
  const hoursDiff = (now.getTime() - updatedAt.getTime()) / (1000 * 60 * 60);
  return hoursDiff > 48;
};
